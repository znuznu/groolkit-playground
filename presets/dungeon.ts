const getGrid = (grid: number[][]): Tile[][] => {
  let tiles: Tile[][] = [];

  for (let i = 0; i < grid.length; i++) {
    tiles[i] = [];
    for (let j = 0; j < grid[0].length; j++) {
      tiles[i][j] = {
        position: { x: i, y: j },
        kind: grid[i][j] ? TileKind.BLOCK : TileKind.EMPTY,
        isCrossed: false
      };
    }
  }

  return tiles;
};

export default getGrid(grid);
